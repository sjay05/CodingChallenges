/* --- XOR (^) OPERATOR --- */
#include <bits/stdc++.h>

using namespace std;

/*
x ^ y produces a number that has one Bitmask in positions
 where exactly one of x and y's have one Bitmask.

 Ex. 22 ^ 16 = 12

    10110 (22)
  ^ 11010 (26)
   ___________
    01100 (12)
 */

int main() {
    printf("%d\n", 22^26);
}


